package mypackage;

public class Patient {
	    private String name;
	    private String healthInfo;  
	    public void setName(String name) {
	        this.name = name;
	    }
	    public String getName() {
	        return name;
	    }
	    public void setHealthInfo(String info) {
	        this.healthInfo = info;
	    }
	    public String getHealthInfo() {
	        return healthInfo;
	    }
	}
